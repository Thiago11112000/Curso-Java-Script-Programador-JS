// Operadores de igualdade e comparação estrita

// Igualdade (==) verifica se os valores são iguais.
console.log(1 == 1); // true
console.log(1 == "1"); // true

// Igualdade estrita (===) verifica se os valores e os tipos são iguais.
console.log(1 === 1); // true
console.log(1 === "1"); // false

// Diferença (!=) verifica se os valores são diferentes.
console.log(1 != "1"); // false
console.log(1 !== 1); // false

// Diferença estrita (!==) verifica se os valores e os tipos são diferentes.
console.log(1 !== "1"); // true
console.log(1 !== 1); // false

// Operadores de comparação

// Maior que (>).
console.log(3 > 2); // true

// Menor que (<).
console.log(3 < 2); // false

// Maior ou igual que (>=).
console.log(2 >= 2); // true

// Menor ou igual que (<=).
console.log(1 <= 2); // true
